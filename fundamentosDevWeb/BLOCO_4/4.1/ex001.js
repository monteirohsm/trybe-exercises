// Faça cinco programas, um para cada operação aritmética básica. Seu programa deve ter duas variáveis, a e b , definidas no começo com os valores que serão operados. Faça programas para:

// Adicao
function adicao(a, b) {
  let soma = a + b;
  return soma;
}
console.log(adicao(2, 3));

// Subtracao
function subtracao(a, b) {
  let subtrair = a - b;
  return subtrair;
}
console.log(subtracao(15, 5));

// Multiplicacao
function multiplicacao(a, b) {
  let multiplicar = a * b;
  return multiplicar;
}
console.log(multiplicacao(5, 5));

// Divisao
function divisao(a, b) {
  let dividir = a / b;
  return dividir;
}
console.log(divisao(40, 2));

// Modulo
function restoDaDivisao(a, b) {
  let modulo = a % b;
  return modulo;
}
console.log(restoDaDivisao(9, 2));
